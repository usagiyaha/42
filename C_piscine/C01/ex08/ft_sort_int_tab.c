void	ft_sort_int_tab(int	*tab, int size)
{
	int	med;
	int	i;

	i = 0;
	while (i < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			med = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = med;
			i = 0;
		}
		else
		{
			i++;
		}
	}
}
