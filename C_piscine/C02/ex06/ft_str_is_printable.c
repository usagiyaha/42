int	ft_str_is_printable(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (str[i])
	{
		if (str[i] < ' ' || str[i] > '~')
		{
			n++;
		}
		i++;
	}
	if (n > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
